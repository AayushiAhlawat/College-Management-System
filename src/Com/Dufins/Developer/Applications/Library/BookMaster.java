    private void BtnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnSaveMouseClicked
        // TODO add your handling code here:
        //validate entry and save record
        try {

            if (TxtBookTitle.getText().trim().length() == 0) {
                JOptionPane.showMessageDialog(null, "Enter book title.");
                return;
            }

            if (TxtAuthor.getText().trim().length() == 0) {
                JOptionPane.showMessageDialog(null, "Enter author name.");
                return;
            }
            if (TxtPublisher.getText().trim().length() == 0) {
                JOptionPane.showMessageDialog(null, "Enter publisher name.");
                return;
            }
            if (CmbCategory.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog(null, "Select book category.");
                return;
            }


            //String strqr="";
            connection = Util.getConnection();
            int newpos = TxtBookID.getText().equals("") ? (rscount + 1) : Integer.parseInt(TxtBookID.getText().trim());
            if (TxtBookID.getText().trim().length() == 0 || newpos > rscount) {

                rsbook.moveToInsertRow();

                rsbook.updateString("title", TxtBookTitle.getText());
                rsbook.updateString("author", TxtAuthor.getText());
                rsbook.updateString("publisher", TxtPublisher.getText());
                rsbook.updateString("publication_year", String.valueOf(TxtYear.getYear()));
                rsbook.updateString("category", CmbCategory.getSelectedItem().toString());
                rsbook.updateString("keyword", TxtKeywords.getText());
                rsbook.updateString("place", TxtPlace.getText());
                rsbook.updateString("edition", TxtEdition.getText());
                rsbook.updateString("price", TxtPrice.getText());
                rsbook.updateString("volume", TxtVolume.getText());
                rsbook.updateString("billno", TxtBillno.getText());
                rsbook.updateString("isbnno", TxtISBNno.getText());

                DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                rsbook.updateString("billdate", df.format(TxtBilldate.getDate()).toString());
                rsbook.updateString("status", CmbStatus.getSelectedItem().toString());
                rsbook.updateString("reason", TxtReason.getText());

                rsbook.insertRow();

                rsbook.last();
                Display();
            } else {
                rsbook.updateString("title", TxtBookTitle.getText());
                rsbook.updateString("author", TxtAuthor.getText());
                rsbook.updateString("publisher", TxtPublisher.getText());
                rsbook.updateString("publication_year", String.valueOf(TxtYear.getYear()));
                rsbook.updateString("category", CmbCategory.getSelectedItem().toString());
                rsbook.updateString("keyword", TxtKeywords.getText());
                rsbook.updateString("place", TxtPlace.getText());
                rsbook.updateString("edition", TxtEdition.getText());
                rsbook.updateString("price", TxtPrice.getText());
                rsbook.updateString("volume", TxtVolume.getText());
                rsbook.updateString("billno", TxtBillno.getText());
                rsbook.updateString("isbnno", TxtISBNno.getText());

                DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                rsbook.updateString("billdate", df.format(TxtBilldate.getDate()).toString());

                if (rsbook.getString("status").equalsIgnoreCase("Receiving") || rsbook.getString("status").equalsIgnoreCase("Issuing") || rsbook.getString("status").equalsIgnoreCase("Issued")) {
                    JOptionPane.showMessageDialog(this, "Can't update because this book is already issued.");
                    return;
                } else {
                    rsbook.updateString("status", CmbStatus.getSelectedItem().toString());
                    rsbook.updateString("reason", TxtReason.getText());
                }

                rsbook.updateRow();
                rsbook.refreshRow();
            }

            JOptionPane.showMessageDialog(null, "Record updated.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Unable to save record.");
            e.printStackTrace();
            return;
        }
        CmdNewMouseClicked(null);
        TxtBookID.setEditable(false);
    }//GEN-LAST:event_BtnSaveMouseClicked

    private void CmdAddCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CmdAddCategoryMouseClicked
        // TODO add your handling code here:
        //AddCategory.setVisible(true);
        String value = JOptionPane.showInputDialog(this, "Add category");
        try {
            connection = Util.getConnection();
            pstmt = connection.prepareStatement("insert into bookcategory values('" + value + "')");
            pstmt.executeUpdate();

            CmbCategory.removeAllItems();
            PreparedStatement stmt = connection.prepareStatement("select * from bookcategory order by category");
            ResultSet rs = stmt.executeQuery();
            while (rs.next()) {
                CmbCategory.addItem(rs.getString(1));
            }
            CmbCategory.setSelectedItem(value);
            rs.close();

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_CmdAddCategoryMouseClicked

