    private void BookTableWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_BookTableWindowClosing
        // TODO add your handling code here
        int row = jTable1.getSelectedRow();
        if (jTable1.getRowCount() > 0) {
            String BookID = jTable1.getValueAt(row, 0).toString();
            try {
                Statement stmt2 = connection.createStatement();
                rsbook = stmt2.executeQuery("select * from bookmaster where book_id = '" + BookID + "'");
                if (rsbook.next()) {
                    Display();
                } else {
                    JOptionPane.showMessageDialog(this, "Data does not available.");
                    ClearText();
                }
                stmt2.close();
                bookdetails();
                rspos = Integer.parseInt(TxtBookID.getText());
                rsbook.absolute(rspos);
                LblPosition.setText("Record position : " + rsbook.getRow() + "/" + String.valueOf(rscount));
            } catch (Exception e) {
                BookTable.setVisible(false);
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Data not available");
        }
    }//GEN-LAST:event_BookTableWindowClosing

