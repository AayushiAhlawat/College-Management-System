    private void jTable_SDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_SDMouseClicked
        // TODO add your handling code here:
        int row = jTable_SD.getSelectedRow();
        if (row >= 0) {
            jPanel2.setVisible(true);
        } else {
            jPanel2.setVisible(false);
        }

        try {
            pst = connection.prepareStatement("SELECT * FROM memberrules WHERE mem_id = " + jTable_SD.getValueAt(row, 0) + "");
            rsrule = pst.executeQuery();
            if (rsrule.next()) {
                txtFine.setText(rsrule.getString(3));
                if (rsrule.getString(4).equals("Monthly")) {
                    RbMonthly.setSelected(true);
                } else if (rsrule.getString(4).equals("Weekly")) {
                    RbWeekly.setSelected(true);
                } else {
                    RbDaily.setSelected(true);
                }
                TxtMBI.setText(rsrule.getString(5));
                TxtMDI.setText(rsrule.getString(6));
                dcExpired.setDate(rsrule.getDate(7));
            } else {
                JOptionPane.showMessageDialog(this, "Data not available.");
                clearText();
            }

        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Data not available.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTable_SDMouseClicked

    private void BtnShowMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnShowMouseClicked
        // TODO add your handling code here:
          // TODO add your handling code here:
        flag = 0;
        try {
            if (Cmb_Member.getSelectedIndex() == 0) {
                pst = connection.prepareStatement("SELECT mem_id, mem_name, mem_email_id, mem_mobile_no, mem_active, mem_type FROM membermaster where mem_id = '" + TxtMember.getText() + "' order by mem_id");
            } else if (Cmb_Member.getSelectedIndex() == 1) {
                pst = connection.prepareStatement("SELECT mem_id, mem_name, mem_email_id, mem_mobile_no, mem_active, mem_type FROM membermaster where mem_name like '" + TxtMember.getText() + "%' order by mem_id");
            } else {
                pst = connection.prepareStatement("SELECT mem_id, mem_name, mem_email_id, mem_mobile_no, mem_active, mem_type FROM membermaster where mem_type = '" + Cmb_MemberType.getSelectedItem().toString() + "'");
                flag = 1;
            }

            rsrule = pst.executeQuery();
            jTable_SD.setModel(net.proteanit.sql.DbUtils.resultSetToTableModel(rsrule));
            Coloumnautosizer.sizeColumnsToFit(jTable_SD);
            jPanel2.setVisible(false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_BtnShowMouseClicked

