        // TODO add your handling code here:
        
        if(dcExpired.getDate().before(new Date()))
        {
            JOptionPane.showMessageDialog(this, "Please check date expired.");
            return;
        }
         int row = jTable_SD.getSelectedRow();
        String selRow = (flag > 0)?" mem_type = '" + Cmb_MemberType.getSelectedItem().toString() + "' ": " mem_id = " + Integer.parseInt(jTable_SD.getValueAt(row, 0).toString()) + "";
        try {
             pst = connection.prepareStatement("UPDATE memberrules SET fine_amount =?, fine_time=?, max_book=?,max_issue_day=?,mem_expirydate=? where " + selRow + "");

            pst.setString(1, txtFine.getText());
            if (RbDaily.isSelected()) {
                pst.setString(2, RbDaily.getText());
            } else if (RbWeekly.isSelected()) {
                pst.setString(2, RbWeekly.getText());
            } else {
                pst.setString(2, RbMonthly.getText());
            }
            pst.setString(3, TxtMBI.getText());
            pst.setString(4, TxtMDI.getText());

            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            pst.setString(5, df.format(dcExpired.getDate()));

            int a = pst.executeUpdate();
            if (a > 0) {
                if (Activation.isSelected()) {
                    pst = connection.prepareStatement("UPDATE membermaster SET mem_active='Y' WHERE " + selRow + "");
                    int b = pst.executeUpdate();
                    if (b > 0) {
                        JOptionPane.showMessageDialog(this, "Member Activated.");
                    }
                }
                else{
                     pst = connection.prepareStatement("UPDATE membermaster SET mem_active='N' WHERE " + selRow + "");
                    int b = pst.executeUpdate();
                    if (b > 0) {
                        JOptionPane.showMessageDialog(this, "Member Deactivated.");
                    }
                }
                
            } else {
                JOptionPane.showMessageDialog(this, "Activation Error.");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        nonActivemem();
        clearText();
