    private void retFacilitySetting() {
        try {
            pstmt = connection.prepareStatement("select * from facility_setting");
            rs = pstmt.executeQuery();
            while (rs.next()) {
                if (rs.getString(1).equals("OT")) {
                    CmbOT.setSelectedItem(rs.getString(2));
                    txtOT.setText(rs.getString(3));
                }
                if (rs.getString(1).equals("PF")) {
                    if (rs.getString(3).contains("%")) {
                        rbPFRate.setSelected(true);
                        txt_RatePF.setText(rs.getString(3).substring(0, rs.getString(3).length() - 1));
                    } else {
                        txt_AmtPF.setText(rs.getString(3).trim());
                        rbPFAmt.setSelected(true);
                    }
                }
                if (rs.getString(1).equals("Mess")) {
                    txt_Mess.setText(rs.getString(3));
                }
                if (rs.getString(1).equals("ESI")) {
                    if (rs.getString(3).contains("%")) {
                        rbESIRate.setSelected(true);
                        txt_RateESI.setText(rs.getString(3).substring(0, rs.getString(3).length() - 1));
                    } else {
                        txt_AmountESI.setText(rs.getString(3));
                        rbESIAmt.setSelected(true);
                    }
                }
                if (rs.getString(1).equals("Conveyance")) {
                    txt_Con.setText(rs.getString(3));
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void retDeductionDetails() {
        try {
            pstmt = connection.prepareStatement("select * from deduction_details");
            rs = pstmt.executeQuery();
            while (rs.next()) {
                if (rs.getString(1).equalsIgnoreCase("HD")) {
                    CmbHH.setSelectedItem(rs.getString(2));
                    CmbHM.setSelectedItem(rs.getString(3));
                }
                if (rs.getString(1).equalsIgnoreCase("LCEL")) {
                    CmbFH.setSelectedItem(rs.getString(2));
                    CmbFM.setSelectedItem(rs.getString(3));
                    CmbLC.setSelectedItem(rs.getString(4));
                    if (rs.getString(5).contains(":HD")) {
                        CmbNHD.setSelectedItem(rs.getString(5).substring(0, rs.getString(5).length() - 3));
                        rbNHD.setSelected(true);
                    } else {
                        CmbNFD.setSelectedItem(rs.getString(5).substring(0, rs.getString(5).length() - 3));
                        rbNFD.setSelected(true);
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

