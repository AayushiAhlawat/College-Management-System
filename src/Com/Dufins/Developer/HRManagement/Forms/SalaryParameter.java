    private void Btn_applySettingMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_applySettingMouseClicked
        // TODO add your handling code here:
        try {
            connection.setAutoCommit(false);
            pstmt = connection.prepareStatement("update facility_setting set max_time = ?, rate = ? where parameter_type = ?");
            //for overtime details
            pstmt.setString(1, CmbOT.getSelectedItem().toString());
            pstmt.setString(2, txtOT.getText().trim());
            pstmt.setString(3, "OT".trim());
            pstmt.addBatch();
            //for PF details
            pstmt.setString(1, "0");
            String pfrate = (rbPFRate.isSelected()) ? txt_RatePF.getText().trim() + "%" : txt_AmtPF.getText().trim();
            pstmt.setString(2, pfrate);
            pstmt.setString(3, "PF".trim());
            pstmt.addBatch();
            //for Mess details
            pstmt.setString(1, "0");
            pstmt.setString(2, txt_Mess.getText().trim());
            pstmt.setString(3, "Mess".trim());
            pstmt.addBatch();
            //for ESI details
            pstmt.setString(1, "0");
            String esirate = (rbESIRate.isSelected()) ? txt_RateESI.getText().trim() + "%" : txt_AmountESI.getText().trim();
            pstmt.setString(2, esirate.trim());
            pstmt.setString(3, "ESI".trim());
            pstmt.addBatch();
            //for conveyance
            pstmt.setString(1, "0");
            pstmt.setString(2, txt_Con.getText().trim());
            pstmt.setString(3, "Conveyance".trim());
            pstmt.addBatch();

            int a[] = pstmt.executeBatch();

            pstmt = connection.prepareStatement("update deduction_details set time_hrs = ?, time_min = ?, set_day = ?, max_day = ? where attribute = ?");
            //for half-day deduction
            pstmt.setString(1, CmbHH.getSelectedItem().toString());
            pstmt.setString(2, CmbHM.getSelectedItem().toString());
            pstmt.setString(3, "0");
            pstmt.setString(4, "0");
            pstmt.setString(5, "HD".trim());
            pstmt.addBatch();
            //for LC/EL
            pstmt.setString(1, CmbFH.getSelectedItem().toString());
            pstmt.setString(2, CmbFM.getSelectedItem().toString());
            pstmt.setString(3, CmbLC.getSelectedItem().toString());
            String maxday = (rbNHD.isSelected()) ? CmbNHD.getSelectedItem().toString() + ":HD" : CmbNFD.getSelectedItem().toString() + ":FD";
            pstmt.setString(4, maxday);
            pstmt.setString(5, "LCEL".trim());
            pstmt.addBatch();

            int b[] = pstmt.executeBatch();
            if (a.length > 0 && b.length > 0) {
                JOptionPane.showMessageDialog(this, "Global setting for salary parameter is successfully updated.");
                retFacilitySetting();
            } else {
                JOptionPane.showMessageDialog(this, "Error while updating salary parameter setting.");
                return;
            }
            connection.commit();
        } catch (SQLException | HeadlessException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_Btn_applySettingMouseClicked

